    private Double getTienPhatTheoThang(String a, String b) {
        Double tienPhat = 0D;

        Statement statement = null;
        ResultSet resultSet = null;
        String sql = "SELECT  sum(tienPhat) FROM muon_tra inner join chitietmuontra on muon_tra.idMuonTra = chitietmuontra.idMuonTra where year(ngayMuon)='" + a + "' and MONTH(ngayMuon)='" + b + "';";
        try {
            statement = connection.createStatement();
            resultSet = statement.executeQuery(sql);
            while (resultSet.next()) {
                tienPhat = resultSet.getDouble("sum(tienPhat)");

            }
        } catch (Exception e) {
            e.printStackTrace();

        }

        return tienPhat;
    }
