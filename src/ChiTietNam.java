    public void xemChiTietNam() {
        bangthongke.removeAll();
        String[] columns = {"Tháng ", "Số lượng hóa đơn", "Tổng tiền cọc", "Tổng tiền phạt"};
        DefaultTableModel model = new DefaultTableModel(columns, 0);
        String sql = "SELECT count(idMuonTra),YEAR(ngayMuon),MONTH(ngayMuon),sum(tienCoc)  FROM muon_tra where YEAR(ngayMuon) ='" + ThongKeMuonTra.docNam() + "'   group by  MONTH(ngayMuon); ";
        Statement statement = null;
        ResultSet resultSet = null;
        ResultSet resultSet1 = null;

        try {
            statement = connection.createStatement();
            resultSet = statement.executeQuery(sql);
            while (resultSet.next()) {
                Vector vector = new Vector();
                vector.add(resultSet.getString("MONTH(ngayMuon)") + "/" + ThongKeMuonTra.docNam());
                vector.add(resultSet.getInt("count(idMuonTra)"));
                vector.add(resultSet.getInt("sum(tienCoc)"));
                vector.add(getTienPhatTheoThang(resultSet.getString("YEAR(ngayMuon)"), resultSet.getString("MONTH(ngayMuon)")));

                model.addRow(vector);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        bangthongke.setModel(model);
        bangthongke.getSelectionModel().addListSelectionListener(new ListSelectionListener() {

            public void valueChanged(ListSelectionEvent e) {

                if (bangthongke.getSelectedRow() >= 0) {
                    thanghien.setText(bangthongke.getValueAt(bangthongke.getSelectedRow(), 0).toString().substring(0, (bangthongke.getValueAt(bangthongke.getSelectedRow(), 0).toString()).length() - 5));

                }

            }

        });
    }

